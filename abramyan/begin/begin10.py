a = int(input("Enter the number a: "))
b = int(input("Enter the number b: "))
a1 = a**2
b1 = b**2
sum = a1+b1
min = abs(a1-b1)
umn = a1*b1
dell = a*a/(b*b)
print(f"The sum of {a}² and {b}² is {sum}.")
print(f"The difference between {a}² and {b}² is {min}.")
print(f"The product of {a}² and {b}² is {umn}.")
print(f"{a}² / {b}² = {dell}.")